// Helper function to create icon element
export function createIcon(name, className = '') {
  const iconHtml = icons[name];
  if (!iconHtml) {
    console.warn(`Icon "${name}" not found`);
    return '';
  }
  
  // Add classes to the SVG
  if (className) {
    return iconHtml.replace('<svg', `<svg class="${className}"`);
  }
  return iconHtml;
}

// Get icon with specific size
export function getIcon(name, size = 'md') {
  const sizeClasses = {
    xs: 'w-3 h-3',
    sm: 'w-4 h-4',
    md: 'w-5 h-5',
    lg: 'w-6 h-6',
    xl: 'w-8 h-8'
  };
  
  return createIcon(name, sizeClasses[size] || sizeClasses.md);
}